// ==================== 辅助函数（Go 特有，Python 不需要）====================

// validateType 检查值是否匹配预期类型
func validateType(value interface{}, expectedType string) bool {
	if value == nil {
		return true
	}

	switch expectedType {
	case "string":
		_, ok := value.(string)
		return ok
	case "integer":
		switch value.(type) {
		case int, int8, int16, int32, int64, uint, uint8, uint16, uint32, uint64:
			return true
		case float64:
			f := value.(float64)
			return f == float64(int(f))
		case string:
			_, err := strconv.ParseInt(value.(string), 10, 64)
			return err == nil
		}
		return false
	case "number":
		switch value.(type) {
		case int, int8, int16, int32, int64, uint, uint8, uint16, uint32, uint64, float32, float64:
			return true
		case string:
			_, err := strconv.ParseFloat(value.(string), 64)
			return err == nil
		}
		return false
	case "boolean":
		switch v := value.(type) {
		case bool:
			return true
		case string:
			lowered := strings.ToLower(v)
			return lowered == "true" || lowered == "false"
		}
		return false
	case "array":
		rv := reflect.ValueOf(value)
		return rv.Kind() == reflect.Slice || rv.Kind() == reflect.Array
	case "object":
		_, ok := value.(map[string]interface{})
		return ok
	default:
		return true
	}
}

// ConvertParameters 将参数从各种类型转换为 map[string]interface{}
func ConvertParameters(params interface{}) (map[string]interface{}, error) {
	switch v := params.(type) {
	case map[string]interface{}:
		return v, nil
	case string:
		var result map[string]interface{}
		if err := json.Unmarshal([]byte(v), &result); err != nil {
			return nil, fmt.Errorf("解析 JSON 参数失败: %w", err)
		}
		return result, nil
	case nil:
		return make(map[string]interface{}), nil
	default:
		rv := reflect.ValueOf(params)
		if rv.Kind() == reflect.Map {
			result := make(map[string]interface{})
			for _, key := range rv.MapKeys() {
				val := rv.MapIndex(key)
				result[key.String()] = val.Interface()
			}
			return result, nil
		}
		return nil, fmt.Errorf("不支持的参数类型: %T", params)
	}
}
